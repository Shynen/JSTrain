let x =7;

if (x < 5) {
    console.log("x est inférieur  5")
} else  {
    console.log("x n'est pas inférieur à 5")
}

if (!x < 5) {  // en ajoutant un ! on inverse la condition (le principe du not)
    console.log("x est inférieur  5")
} else  {
    console.log("x n'est pas inférieur à 5")
}